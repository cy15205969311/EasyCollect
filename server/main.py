def dump_raw_payload(raw: Any) -> Path:
    """Persist the latest raw 1688 payload to a local JSON debug file.

    The file is intentionally overwritten on each capture during the MVP phase so
    developers can inspect the most recent real-world payload shape without
    introducing a database or long-term storage layer.

    Args:
        raw: Raw JSON-compatible value received from the browser extension.

    Returns:
        The absolute path to the written JSON payload file.
    """

    CACHE_DATA_DIR.mkdir(parents=True, exist_ok=True)

    with RAW_PAYLOAD_PATH.open("w", encoding="utf-8") as file:
        json.dump(raw, file, indent=2, ensure_ascii=False, default=str)

    return RAW_PAYLOAD_PATH


