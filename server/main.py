def infer_product_platform(product: dict[str, Any], source_file: str) -> str:
    """Infer a product platform label from cached product content."""

    explicit = product.get("platform")
    if isinstance(explicit, str) and explicit.strip():
        platform = explicit.strip().lower()
        if "shopee" in platform:
            return "Shopee"
        if "1688" in platform:
            return "1688"

    image_blob = json.dumps(product.get("main_images", []), ensure_ascii=False)
    if "shopee" in image_blob or "cf.shopee" in image_blob:
        return "Shopee"
    if "alicdn" in image_blob or "1688" in source_file:
        return "1688"

    return "Unknown"


def product_cache_to_summary(cache_path: Path) -> dict[str, Any] | None:
    """Convert a local product JSON cache file into dashboard row data."""

    if not cache_path.exists():
        return None

    try:
        with cache_path.open("r", encoding="utf-8") as file:
            product = json.load(file)
    except (OSError, json.JSONDecodeError) as exc:
        logger.warning("Failed to read product cache %s: %s", cache_path, exc)
        return None

    if not isinstance(product, dict):
        return None

    title = (
        product.get("title_optimized")
        or product.get("title")
        or product.get("source_title")
        or "Untitled Product"
    )
    stat = cache_path.stat()

    return {
        "id": cache_path.stem,
        "title": product.get("title") or title,
        "title_optimized": product.get("title_optimized"),
        "source_title": product.get("source_title"),
        "platform": infer_product_platform(product, cache_path.name),
        "base_price": product.get("base_price") or "",
        "main_images": product.get("main_images") or [],
        "sku_list": product.get("sku_list") or [],
        "marketing_copy": product.get("marketing_copy"),
        "bullet_points": product.get("bullet_points") or [],
        "platform_tags": product.get("platform_tags") or [],
        "source_file": cache_path.name,
        "updated_at": datetime.fromtimestamp(stat.st_mtime).isoformat(timespec="seconds"),
    }


def list_local_product_summaries() -> list[dict[str, Any]]:
    """Read current local product cache files for the dashboard MVP."""

    cache_files = [
        CACHE_DATA_DIR / "optimized_product.json",
        CACHE_DATA_DIR / "parsed_product.json",
    ]
    products: list[dict[str, Any]] = []
    seen_ids: set[str] = set()

    for cache_path in cache_files:
        product = product_cache_to_summary(cache_path)
        if not product or product["id"] in seen_ids:
            continue

        seen_ids.add(product["id"])
        products.append(product)

    return products


def safe_get(data: Any, path: list[str]) -> Any:
    """Read a nested dictionary path without raising key or type errors.

    Args:
        data: Candidate dictionary-like value to inspect.
        path: Sequence of dictionary keys to follow.

    Returns:
        The nested value when every path segment exists, otherwise ``None``.
    """

    current = data

    for key in path:
        if not isinstance(current, dict):
            return None

        current = current.get(key)

    return current


def safe_get_any(data: Any, paths: list[list[str]]) -> Any:
    """Return the first non-empty value from a list of nested dictionary paths.

    Args:
        data: Candidate dictionary-like value to inspect.
        paths: Ordered nested paths to try.

    Returns:
        The first non-empty nested value, otherwise ``None``.
    """

    for path in paths:
        value = safe_get(data, path)
        if value not in (None, "", [], {}):
            return value

    return None


def find_values_by_key(data: Any, target_key: str) -> list[Any]:
    """Recursively collect all values whose dictionary key matches ``target_key``.

    Args:
        data: Raw product payload or nested child value.
        target_key: Exact key name to search for.

    Returns:
        A list of matching values in depth-first traversal order.
    """

    matches: list[Any] = []

    def walk(node: Any) -> None:
        if isinstance(node, dict):
            for key, value in node.items():
                if key == target_key:
                    matches.append(value)
                walk(value)
        elif isinstance(node, list):
            for item in node:
                walk(item)

    walk(data)
    return matches


def find_first_by_keys(data: Any, keys: list[str]) -> Any:
    """Recursively return the first non-empty value for any key in ``keys``.

    Args:
        data: Raw product payload or nested child value.
        keys: Candidate key names ordered from most to least specific.

    Returns:
        The first discovered non-empty value, otherwise ``None``.
    """

    key_set = set(keys)

    def walk(node: Any) -> Any:
        if isinstance(node, dict):
            for key in keys:
                value = node.get(key)
                if value not in (None, "", [], {}):
                    return value

            for key, value in node.items():
                if key in key_set:
                    continue

                found = walk(value)
                if found not in (None, "", [], {}):
                    return found
        elif isinstance(node, list):
            for item in node:
                found = walk(item)
                if found not in (None, "", [], {}):
                    return found

        return None

    return walk(data)


def normalize_title(value: Any) -> str | None:
    """Normalize a candidate title value into a readable string.

    Args:
        value: Candidate title-like value discovered in the raw payload.

    Returns:
        A stripped title string when available, otherwise ``None``.
    """

    if isinstance(value, str):
        title = value.strip()
        return title or None

    return None


def normalize_price(value: Any) -> str | int | float | None:
    """Normalize a candidate price value into a compact scalar response value.

    Args:
        value: Candidate price-like value discovered in the raw payload.

    Returns:
        A string or numeric price when available, otherwise ``None``.
    """

    if isinstance(value, (int, float)):
        return value

    if isinstance(value, str):
        price = value.strip()
        return price or None

    if isinstance(value, list):
        for item in value:
            normalized = normalize_price(item)
            if normalized is not None:
                return normalized

    if isinstance(value, dict):
        for key in [
            "priceDisplay",
            "offerPriceDisplay",
            "refPrice",
            "price",
            "priceText",
            "value",
            "minPrice",
            "offerMinPrice",
            "maxPrice",
            "offerMaxPrice",
        ]:
            normalized = normalize_price(value.get(key))
            if normalized is not None:
                return normalized

    return None


def normalize_shopee_price(value: Any) -> str | None:
    """Convert Shopee integer price units into a human-readable decimal string.

    Shopee item payloads commonly store prices multiplied by ``100000``. For
    example, ``1639000`` represents ``16.39``.

    Args:
        value: Raw Shopee price value.

    Returns:
        Decimal price string, or ``None`` when unavailable.
    """

    if value in (None, "", [], {}):
        return None

    try:
        number = float(value)
    except (TypeError, ValueError):
        return None

    if number <= 0:
        return None

    if number >= 100000:
        number = number / 100000

    return f"{number:.2f}".rstrip("0").rstrip(".")


def normalize_shopee_price_range(price_min: Any, price_max: Any) -> str | None:
    """Format Shopee min/max price fields as a compact price range string."""

    min_price = normalize_shopee_price(price_min)
    max_price = normalize_shopee_price(price_max)

    if min_price and max_price and min_price != max_price:
        return f"{min_price}-{max_price}"

    return min_price or max_price


def normalize_shopee_dom_price(value: Any) -> str | None:
    """Normalize visible Shopee price text such as ``RM5.40 - RM10.00``."""

    if not isinstance(value, str):
        return None

    prices = [
        parse_standard_price(match.group(0))
        for match in re.finditer(r"\d+(?:[.,]\d+)?", value.replace(",", ""))
    ]
    clean_prices = [price for price in prices if price is not None]
    if not clean_prices:
        return None

    min_price = min(clean_prices)
    max_price = max(clean_prices)
    if min_price == max_price:
        return format_standard_price(min_price)

    return f"{format_standard_price(min_price)}-{format_standard_price(max_price)}"


def extract_shopee_dom_price(raw_data: Any, item: dict[str, Any]) -> str | None:
    """Read Shopee DOM price fallback from wrapped frontend payloads."""

    candidates: list[Any] = [item.get("dom_price"), item.get("display_price")]
    if isinstance(raw_data, dict):
        candidates.extend(
            [
                raw_data.get("dom_price"),
                raw_data.get("display_price"),
                safe_get(raw_data, ["data", "dom_price"]),
                safe_get(raw_data, ["data", "display_price"]),
            ]
        )

    for candidate in candidates:
        price = normalize_shopee_dom_price(candidate)
        if price:
            return price

    return None


def extract_shopee_base_price(item: dict[str, Any]) -> str | None:
    """Extract Shopee product-level price from common single/range fields."""

    for key in [
        "price",
        "price_before_discount",
        "raw_price",
        "current_price",
        "min_price",
        "max_price",
    ]:
        price = normalize_shopee_price(item.get(key))
        if price:
            return price

    range_candidates = [
        ("price_min", "price_max"),
        ("min_price", "max_price"),
        ("price_min_before_discount", "price_max_before_discount"),
    ]
    for min_key, max_key in range_candidates:
        price_range = normalize_shopee_price_range(item.get(min_key), item.get(max_key))
        if price_range:
            return price_range

    price_stocks = item.get("price_stocks")
    if isinstance(price_stocks, list):
        for price_stock in price_stocks:
            if not isinstance(price_stock, dict):
                continue

            price = normalize_shopee_price(
                price_stock.get("price")
                or price_stock.get("current_price")
                or price_stock.get("price_before_discount")
            )
            if price:
                return price

    return None


def parse_standard_price(value: Any) -> float | None:
    """Parse a normalized or raw marketplace price into a comparable float.

    Shopee can expose raw integer prices multiplied by ``100000``. This helper
    accepts already-normalized values such as ``15.90`` and raw values such as
    ``1590000`` so downstream healing can compare prices safely.
    """

    if value in (None, "", [], {}):
        return None

    if isinstance(value, (int, float)):
        number = float(value)
    elif isinstance(value, str):
        cleaned = value.strip().lower()
        if cleaned in {"not found", "none", "null", "nan", "0", "0.0", "0.00"}:
            return None

        match = re.search(r"\d+(?:[.,]\d+)?", cleaned.replace(",", ""))
        if not match:
            return None

        try:
            number = float(match.group(0))
        except ValueError:
            return None
    else:
        return None

    if number <= 0:
        return None

    if number >= 100000:
        number = number / 100000

    return number if number > 0 else None


def format_standard_price(value: float) -> str:
    """Format a marketplace price with two decimal places for cache and CSV."""

    return f"{value:.2f}"


def is_missing_base_price(value: Any) -> bool:
    """Return whether a product-level price needs SKU-based self-healing."""

    if value in (None, "", [], {}):
        return True

    if isinstance(value, str) and value.strip().lower() in {
        "not found",
        "none",
        "null",
        "nan",
        "0",
        "0.0",
        "0.00",
    }:
        return True

    return parse_standard_price(value) is None


def normalize_base_price_value(value: Any) -> str:
    """Normalize a base price or price range without losing range semantics."""

    if isinstance(value, str) and "-" in value:
        parts = [parse_standard_price(part) for part in value.split("-", 1)]
        if parts[0] is not None and parts[1] is not None:
            if parts[0] == parts[1]:
                return format_standard_price(parts[0])
            return f"{format_standard_price(parts[0])}-{format_standard_price(parts[1])}"

    price = parse_standard_price(value)
    return "" if price is None else format_standard_price(price)


def heal_product_prices(clean_data: dict[str, Any]) -> dict[str, Any]:
    """Normalize SKU prices and infer ``base_price`` from SKU rows when needed."""

    sku_list = clean_data.get("sku_list")
    if not isinstance(sku_list, list):
        sku_list = []
        clean_data["sku_list"] = sku_list

    sku_prices: list[float] = []
    for sku in sku_list:
        if not isinstance(sku, dict):
            continue

        price = parse_standard_price(sku.get("price"))
        if price is None:
            sku["price"] = ""
            continue

        sku["price"] = format_standard_price(price)
        sku_prices.append(price)

    base_price = clean_data.get("base_price")
    if is_missing_base_price(base_price) and sku_prices:
        min_price = min(sku_prices)
        max_price = max(sku_prices)
        clean_data["base_price"] = (
            format_standard_price(min_price)
            if min_price == max_price
            else f"{format_standard_price(min_price)}-{format_standard_price(max_price)}"
        )
    else:
        normalized_base_price = normalize_base_price_value(base_price)
        if normalized_base_price:
            clean_data["base_price"] = normalized_base_price

    return clean_data


def infer_price_range_from_skus(sku_list: list[dict[str, Any]]) -> str | None:
    """Infer a product price or price range from normalized SKU row prices."""

    prices = [
        price
        for sku in sku_list
        if isinstance(sku, dict)
        for price in [parse_standard_price(sku.get("price"))]
        if price is not None
    ]
    if not prices:
        return None

    min_price = min(prices)
    max_price = max(prices)
    if min_price == max_price:
        return format_standard_price(min_price)

    return f"{format_standard_price(min_price)}-{format_standard_price(max_price)}"


def normalize_shopee_image(value: Any) -> str | None:
    """Normalize a Shopee image ID or URL into an absolute image URL."""

    if not isinstance(value, str):
        return None

    image = value.strip()
    if not image:
        return None

    if image.startswith("//"):
        return f"https:{image}"

    if image.startswith(("http://", "https://")):
        return image

    return f"{SHOPEE_IMAGE_BASE_URL}{image}"


def collect_image_urls(value: Any) -> list[str]:
    """Extract image URLs from strings, lists, and dictionary-shaped image nodes.

    Args:
        value: Candidate image container from the raw 1688 payload.

    Returns:
        A de-duplicated list of image URLs preserving discovery order.
    """

    images: list[str] = []
    seen: set[str] = set()
    image_keys = {
        "url",
        "imgUrl",
        "imageUrl",
        "imageURL",
        "picUrl",
        "picURL",
        "originalUrl",
        "originalURL",
        "original",
        "bigImageUrl",
        "bigImageURL",
        "originalImageURI",
        "originalImageUri",
        "summImageUrl",
        "summImageURL",
        "fullPathImageURI",
        "imageURI",
        "imageUri",
        "mainImage",
        "mainImageUrl",
    }

    def add(candidate: str) -> None:
        url = candidate.strip()

        if url.startswith("//"):
            url = f"https:{url}"

        if url.startswith("img/") or url.startswith("bao/uploaded/"):
            url = f"https://cbu01.alicdn.com/{url}"

        if not url.startswith(("http://", "https://")):
            return

        if url not in seen:
            seen.add(url)
            images.append(url)

    def walk(node: Any) -> None:
        if isinstance(node, str):
            add(node)
        elif isinstance(node, list):
            for item in node:
                walk(item)
        elif isinstance(node, dict):
            for key in image_keys:
                value = node.get(key)
                if isinstance(value, str):
                    add(value)

            for value in node.values():
                if isinstance(value, (dict, list)):
                    walk(value)

    walk(value)
    return images


def normalize_spec_name(value: Any) -> str:
    """Normalize SKU specification text for the parsed product cache.

    Args:
        value: Raw specification value from SKU maps, often HTML-escaped by 1688.

    Returns:
        A readable specification string, or an empty string when unavailable.
    """

    if value is None:
        return ""

    return unescape(str(value)).strip()


def normalize_lookup_key(value: Any) -> str:
    """Normalize option/spec aliases for fuzzy SKU image lookup."""

    return re.sub(r"\s+", " ", normalize_spec_name(value)).strip().lower()


def normalize_coord_key(value: Any) -> str:
    """Normalize 1688 property-value ids used for index-based SKU mapping."""

    if value in (None, "", [], {}):
        return ""

    return str(value).strip()


def extract_coord_keys(value: Any) -> list[str]:
    """Extract possible 1688 property coordinate ids from strings/lists/dicts."""

    keys: list[str] = []
    seen: set[str] = set()

    def remember(candidate: Any) -> None:
        key = normalize_coord_key(candidate)
        if key and key not in seen:
            seen.add(key)
            keys.append(key)

    def add(candidate: Any) -> None:
        text = normalize_coord_key(candidate)
        if not text:
            return

        remember(text)
        for token in re.split(r"[&;,|>\s]+", text):
            remember(token)
            for separator in [":", "="]:
                if separator in token:
                    right_side = token.rsplit(separator, 1)[-1]
                    remember(right_side)

    if isinstance(value, dict):
        for key in [
            "specId",
            "specID",
            "propId",
            "propID",
            "propIds",
            "propValueId",
            "propValueID",
            "propertyValueId",
            "propertyValueID",
            "skuProps",
            "skuPropIds",
            "valueId",
            "valueID",
            "vid",
            "id",
        ]:
            add(value.get(key))
    elif isinstance(value, list):
        for item in value:
            add(item)
    else:
        add(value)

    return keys


def normalize_stock(value: Any) -> str:
    """Normalize SKU stock-like values into a display-friendly string.

    Args:
        value: Raw stock value such as ``canBookCount`` or ``stock``.

    Returns:
        A string representation for the parsed SKU cache.
    """

    if value is None:
        return ""

    return str(value).strip()


def get_first_sku_value(sku: dict[str, Any], keys: list[str]) -> Any:
    """Read the first non-empty SKU value from a list of candidate keys.

    Args:
        sku: Raw SKU dictionary.
        keys: Ordered candidate key names.

    Returns:
        The first non-empty SKU field value, otherwise ``None``.
    """

    for key in keys:
        value = sku.get(key)
        if value not in (None, "", [], {}):
            return value

    return None


def normalize_sku_row(spec_key: str, sku: dict[str, Any]) -> dict[str, Any]:
    """Convert a raw 1688 SKU dictionary into the compact cache row shape.

    Args:
        spec_key: Fallback specification name from map-style SKU containers.
        sku: Raw SKU record containing price, stock, and optional image fields.

    Returns:
        A normalized SKU row with ``spec_name``, ``price``, ``stock``, and
        ``sku_image`` fields.
    """

    spec_name = normalize_spec_name(
        get_first_sku_value(sku, ["specAttrs", "specName", "name", "title"]) or spec_key
    )
    price = normalize_price(
        get_first_sku_value(sku, ["discountPrice", "promotionPrice", "price", "retailPrice"])
    )
    stock = normalize_stock(
        get_first_sku_value(
            sku,
            ["canBookCount", "stock", "stockCount", "inventory", "quantity", "amount"],
        )
    )
    sku_images = collect_image_urls(
        get_first_sku_value(
            sku,
            [
                "skuImage",
                "skuImageUrl",
                "skuImageURI",
                "skuImageUri",
                "sku_image",
                "sku_image_url",
                "sku_image_uri",
                "image",
                "imageUrl",
                "imageURL",
                "imageURI",
                "imageUri",
                "image_url",
                "image_uri",
                "picUrl",
                "picURL",
                "pic_url",
                "fullPathImageURI",
                "fullPathImageUri",
                "originalImageURI",
                "originalImageUri",
                "originalImageUrl",
            ],
        )
    )

    coord_keys = extract_coord_keys(spec_key)
    coord_keys.extend(
        key
        for key in extract_coord_keys(sku)
        if key not in coord_keys
    )

    return {
        "spec_name": spec_name,
        "price": "" if price is None else str(price),
        "stock": stock,
        "sku_image": sku_images[0] if sku_images else "",
        "_coord_keys": coord_keys,
    }


def normalize_sku_container(value: Any) -> list[dict[str, Any]]:
    """Normalize a SKU map or list from 1688 into compact parsed rows.

    Args:
        value: Candidate SKU container, either a dict keyed by spec name or a
            list of SKU dictionaries.

    Returns:
        A list of cleaned SKU rows.
    """

    rows: list[dict[str, Any]] = []
    seen: set[tuple[str, str, str]] = set()

    def add_row(spec_key: str, sku: Any) -> None:
        if not isinstance(sku, dict):
            return

        row = normalize_sku_row(spec_key, sku)
        identity = (row["spec_name"], row["price"], row["stock"])

        if not row["spec_name"] or identity in seen:
            return

        seen.add(identity)
        rows.append(row)

    if isinstance(value, dict):
        for spec_key, sku in value.items():
            add_row(str(spec_key), sku)
    elif isinstance(value, list):
        for sku in value:
            add_row("", sku)

    return rows


def add_1688_sku_image_aliases(
    image_map: dict[str, str],
    aliases: list[Any],
    image_value: Any,
) -> None:
    """Register SKU option aliases against the first discovered image URL."""

    image_urls = collect_image_urls(image_value)
    if not image_urls:
        return

    image_url = image_urls[0]
    for alias in aliases:
        lookup_key = normalize_lookup_key(alias)
        if lookup_key and lookup_key not in image_map:
            image_map[lookup_key] = image_url

        for coord_key in extract_coord_keys(alias):
            if coord_key and coord_key not in image_map:
                image_map[coord_key] = image_url


